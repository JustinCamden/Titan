<script>
   import localize from '~/helpers/utility-functions/Localize.js';
   import Select from '~/helpers/svelte-components/select/Select.svelte';

   /** @type string The value that this input should modify. */
   export let value = void 0;

   /** @type boolean Whether to allow None as an option. */
   export let allowNone = false;

   /** @type boolean Whether the input should currently be disabled. */
   export let disabled = false;

   /** @type SelectOption[] Options for the Select. */
   const options = [
      {
         value: 'armor',
         label: localize('armor'),
      },
      {
         value: 'commodity',
         label: localize('commodity'),
      },
      {
         value: 'equipment',
         label: localize('equipment'),
      },
      {
         value: 'shield',
         label: localize('shield'),
      },
      {
         value: 'weapon',
         label: localize('weapon'),
      },
   ];

   // Add None option if appropriate.
   if (allowNone) {
      options.push({
         label: localize('none'),
         value: 'none',
      });
   }
</script>

<Select bind:value {disabled} on:change {options}/>
